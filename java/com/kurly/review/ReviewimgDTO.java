package com.kurly.review;

public class ReviewimgDTO {

	private Long rvdIdx; // 리뷰 이미지 번호
	private Long rvdRvidx; // 리뷰 번호. ReviewDTO. foreign key
	private String rvdFile; // 리뷰 이미지 경로
	
	public Long getRvdIdx() {
		return rvdIdx;
	}
	public void setRvdIdx(Long rvdIdx) {
		this.rvdIdx = rvdIdx;
	}
	public Long getRvdRvidx() {
		return rvdRvidx;
	}
	public void setRvdRvidx(Long rvdRvidx) {
		this.rvdRvidx = rvdRvidx;
	}
	public String getRvdFile() {
		return rvdFile;
	}
	public void setRvdFile(String rvdFile) {
		this.rvdFile = rvdFile;
	}
	
	@Override
	public String toString() {
		return "ReviewimgDTO [rvdIdx=" + rvdIdx + ", rvdRvidx=" + rvdRvidx + ", rvdFile=" + rvdFile + "]";
	}
}
